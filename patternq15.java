public class patternq15 {
    public static void main(String[] args) {
        int n=9;
    int num=1;
    for (int i=1;i<=n;i++){
        for (int j=1;j<=i;j++){
            System.out.print(num%10+" ");
            num++;
        }
        System.out.println();
    }
    }
    
    
}
